info=imfinfo(str); 										%%参数必须是字符串
registered=imtransform(im,t_concord,'XData',[1 info.Width],'YData',[1 info.Height]); 
%%进行图像配准
axes(handles.axes2); %%弹出图片        
imshow(registered); 			%%显示处理后的结果



function pushbutton3_Callback(hObject, eventdata, handles) % 几何变换
global im img2
img2 = im ;
a=menu('几何变换类型','图像平移','图像缩放','图像旋转','图像配准');
if a==1
    Hs=str2double(get(handles.edit3,'string'));
    Vs=str2double(get(handles.edit4,'string'));
    se = translate(strel(1),[Vs,Hs]);
    img2 = imdilate(im,se);
    axes(handles.axes2);  
    imshow(img2);
elseif a==2
    [srcrow,srccol] = size(im)
    scale=str2double(get(handles.edit5,'string'));
    img2 = imresize(im,scale,'bilinear');
    %axes(handles.axes2);
    [dstrow,dstcol] = size(img2)
    figure(2);
    imshow(img2);
elseif a==3
    angle=str2double(get(handles.edit7,'string'));
    img2 = imrotate(im,angle,'bilinear','crop');
    axes(handles.axes2);  
    imshow(img2);
else
    dataconvertresult=im2double(im);
end

